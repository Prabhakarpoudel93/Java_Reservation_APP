    private void jButton_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_loginActionPerformed
        // TODO add your handling code here:
        String username=jTextField_userNameWaiter.getText();
        String password=jPasswordField_password.getText();
        LoginWaiters lw=new LoginWaiters();
        lw.setUsername(username);
        lw.setPassword(password);
        LoginWaiterDAO lwdao=new LoginWaiterDAO();
        int count= lwdao.login(lw);
        if(count>0)
        {
            Global.waiter_name=username;
            Global.waiter_id=count;
            this.dispose();
            new MainFrameWaiter().setVisible(true);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Username or Password donot matches");
        }
        
    }//GEN-LAST:event_jButton_loginActionPerformed

